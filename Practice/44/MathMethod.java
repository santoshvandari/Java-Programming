class MathMethod{
    public static void main(String args[]){
        System.out.println("Absolute Value = "+Math.abs(-46));
        System.out.println("Square Root = "+Math.sqrt(25));
        System.out.println("Cube Root = "+Math.cbrt(125));
        System.out.println("Floor Value = "+Math.floor(2.125)); //removes the decimal
        System.out.println("Log Value = "+Math.log(10));
        System.out.println("Ceil Value = "+Math.ceil(77.11));
        System.out.println("Power Value = "+Math.pow(4,3));
        System.out.println("Minimum Value = "+Math.min(10,7));
        System.out.println("Maximum Value = "+Math.max(10,7));
        System.out.println("Round Value = "+Math.round(77.50));
   }
}
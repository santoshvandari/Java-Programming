import java.util.HashSet;
class HashSetDemo{
    public static void main(String args[]){
        HashSet<String> set=new HashSet<String>();
        set.add("Maths");
        set.add("Science");
        set.add("Nepali");
        set.add("English");
        set.add("Social");
        System.out.println(set);
    }
}
//WAP to find sum of Two Numbers
import java.util.Scanner;
public class Sum{
    public static void main(String[] args){
        Scanner sc=new Scanner(System.in);
        int a,b;
        System.out.println("Enter Two Numbers:");
        a=sc.nextInt();
        b=sc.nextInt();
        System.out.print("Sum = "+(a+b));
    }
}